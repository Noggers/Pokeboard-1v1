    public static CardView[] generateAllStadiumCardViewModels()
    {        
    	Card[] cards = CardFactory.generateAllStadiumCards();
    	CardView[] cardViews = new CardView[cards.length];
    	for(int index = 0; index < cards.length; ++index)
    	{
    		cardViews[index] = new CardView(cards[index], "/images/stadiums/" + cards[index].getTitle());
    	}
    	return cardViews;
    }
    
